function qs(sel) { return document.querySelector(sel); }

function escapeHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function setHTML(sel, html) {
  const el = typeof sel === 'string' ? qs(sel) : sel;
  if (el) el.innerHTML = html;
}

function showError(container, errObj) {
  const el = typeof container === 'string' ? qs(container) : container;
  if (!el) return;
  const msg = errObj.error || 'Unknown error';
  const provider = errObj.provider ? ' (provider: ' + errObj.provider + ')' : '';
  el.innerHTML = '<div class="results-error">' + escapeHtml(msg + provider) + '</div>';
}

// ---------------------------------------------------------------------------
// Score color interpolation (green #4A7B4A at 0.0, red #8B2E2E at 1.0)
// ---------------------------------------------------------------------------

function scoreColor(score) {
  const r = Math.round(0x4A + (0x8B - 0x4A) * score);
  const g = Math.round(0x7B + (0x2E - 0x7B) * score);
  const b = Math.round(0x4A + (0x2E - 0x4A) * score);
  return 'rgb(' + r + ',' + g + ',' + b + ')';
}

// ---------------------------------------------------------------------------
// API
// ---------------------------------------------------------------------------

async function apiFetch(path, opts) {
  const resp = await fetch(path, opts || {});
  return resp.json();
}

async function apiRun(scenarioName, modelAlias, n) {
  return apiFetch('/api/run', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ scenario_name: scenarioName, model_alias: modelAlias, n: n }),
  });
}

async function apiScore(sessionId) {
  return apiFetch('/api/score', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ session_id: sessionId }),
  });
}

async function apiRunComparison(scenarioName, modelAliases, n) {
  return apiFetch('/api/run-comparison', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ scenario_name: scenarioName, model_aliases: modelAliases, n: n }),
  });
}

async function apiRunMultiplayer(scenarioName, persuaderAlias, skepticAlias) {
  return apiFetch('/api/run-multiplayer', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      scenario_name: scenarioName,
      persuader_alias: persuaderAlias,
      skeptic_alias: skepticAlias,
    }),
  });
}

async function apiReport(sessionId) {
  const resp = await fetch('/api/report/' + sessionId);
  if (!resp.ok) return null;
  return resp.text();
}

