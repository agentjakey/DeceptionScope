async function runComparison() {
  const scenario = state.selectedScenario;
  const aliases = Array.from(state.compareModels);
  const n = parseInt(qs('#compare-n').value, 10) || 5;
  if (!scenario || aliases.length < 2) return;

  const resultsEl = qs('#compare-results');
  resultsEl.innerHTML = RESEARCH_NOTE +
    '<div class="results-loading">Running comparison across ' + aliases.length + ' models...</div>';

  const btn = qs('#compare-btn');
  btn.disabled = true;
  btn.textContent = 'Running...';

  try {
    const results = await apiRunComparison(scenario.name, aliases, n);
    if (results.error) { showError(resultsEl, results); return; }

    const allScores = {};
    for (let i = 0; i < aliases.length; i++) {
      const alias = aliases[i];
      const session = results[alias];
      if (session && session.session_id) {
        try { allScores[alias] = await apiScore(session.session_id); } catch (e) { allScores[alias] = null; }
      }
    }

    renderComparisonResults(resultsEl, results, allScores, scenario, aliases);
  } catch (err) {
    showError(resultsEl, { error: err.message || 'Request failed' });
  } finally {
    btn.disabled = false;
    btn.textContent = 'Run Comparison';
    updateButtonStates();
  }
}

function renderComparisonResults(container, results, allScores, scenario, aliases) {
  const dimNames = (scenario.evaluation_dimensions || []).map(function(d) { return d.name; });
  let html = RESEARCH_NOTE;

  html += '<div class="comparison-models-row">';
  aliases.forEach(function(alias, idx) {
    const scores = allScores[alias];
    const stats = scores ? computeStats(scores, dimNames) : {};
    const tableHtml = Object.keys(stats).length
      ? renderCompactSummaryTable(stats)
      : '<p class="score-failed-label">Scoring unavailable</p>';
    const nameClass = 'comparison-model-name' + (idx > 0 ? ' is-secondary' : '');
    html += '<div class="comparison-model-col">' +
      '<div class="' + nameClass + '">' + escapeHtml(alias) + '</div>' +
      tableHtml + '</div>';
  });
  html += '</div>';

  html += '<div class="chart-container">' +
    '<div class="section-heading">Score Comparison</div>' +
    '<canvas id="compare-chart" class="chart-canvas"></canvas></div>';

  container.innerHTML = html;
  buildComparisonChart(allScores, aliases, dimNames);
}

function renderCompactSummaryTable(stats) {
  const rows = Object.keys(stats).map(function(dim) {
    const s = stats[dim];
    const cls = s.mean > 0.6 ? ' class="cell-high"' : s.mean < 0.2 ? ' class="cell-low"' : '';
    return '<tr><td>' + escapeHtml(dim) + '</td>' +
      '<td' + cls + '>' + s.mean.toFixed(3) + '</td>' +
      '<td>' + s.min.toFixed(3) + '</td>' +
      '<td>' + s.max.toFixed(3) + '</td></tr>';
  }).join('');
  return '<table class="summary-table"><thead>' +
    '<tr><th>Dimension</th><th>Mean</th><th>Min</th><th>Max</th></tr>' +
    '</thead><tbody>' + rows + '</tbody></table>';
}

function buildComparisonChart(allScores, aliases, dimNames) {
  const canvas = document.getElementById('compare-chart');
  if (!canvas || !window.Chart) return;

  if (state.compareChart) { state.compareChart.destroy(); state.compareChart = null; }

  const COLORS = ['#8B2E2E', '#6B7B8D', '#4A7B4A', '#7A5C3A'];

  const datasets = aliases.map(function(alias, idx) {
    const scores = allScores[alias];
    const stats = scores ? computeStats(scores, dimNames) : {};
    const data = dimNames.map(function(dim) {
      return stats[dim] ? parseFloat(stats[dim].mean.toFixed(3)) : 0;
    });
    const color = COLORS[idx] || COLORS[1];
    return {
      label: alias,
      data: data,
      backgroundColor: color + '22',
      borderColor: color,
      borderWidth: 1,
    };
  });

  state.compareChart = new Chart(canvas, {
    type: 'bar',
    data: { labels: dimNames, datasets: datasets },
    options: {
      animation: { duration: 0 },
      responsive: true,
      maintainAspectRatio: true,
      scales: {
        y: {
          min: 0,
          max: 1,
          ticks: { font: { family: "'IBM Plex Mono'" }, color: '#7A7570' },
          grid: { color: '#D4CFC8' },
        },
        x: {
          ticks: {
            font: { family: "'IBM Plex Mono'", size: 10 },
            color: '#7A7570',
            maxRotation: 45,
          },
          grid: { color: '#D4CFC8' },
        },
      },
      plugins: {
        legend: {
          labels: { font: { family: "'IBM Plex Mono'" }, color: '#1A1A18' },
        },
      },
    },
  });
}

