async function runSession() {
  const scenario = state.selectedScenario;
  const modelAlias = state.runModel;
  const n = parseInt(qs('#run-n').value, 10) || 8;
  if (!scenario || !modelAlias) return;

  const resultsEl = qs('#run-results');
  resultsEl.innerHTML = RESEARCH_NOTE +
    '<div class="results-loading">Running... this may take a minute.</div>';

  const btn = qs('#run-btn');
  btn.disabled = true;
  btn.textContent = 'Running...';

  try {
    const payload = { scenario_name: scenario.name, model_alias: modelAlias, n: n };
    console.log('[DeceptionScope] POST /api/run payload:', payload);
    const result = await apiRun(scenario.name, modelAlias, n);
    if (result.error) { showError(resultsEl, result); return; }

    resultsEl.innerHTML = RESEARCH_NOTE +
      '<div class="results-loading">Scoring completions...</div>';

    let scores = null;
    try { scores = await apiScore(result.session_id); } catch (e) { /* render without scores */ }

    renderBonResults(resultsEl, result, scores, scenario);
  } catch (err) {
    showError(resultsEl, { error: err.message || 'Request failed' });
  } finally {
    btn.disabled = false;
    btn.textContent = 'Run Session';
    updateButtonStates();
  }
}

function renderBonResults(container, result, scores, scenario) {
  const dimNames = (scenario.evaluation_dimensions || []).map(function(d) { return d.name; });
  const n = result.completions.length;
  let html = RESEARCH_NOTE;

  result.completions.forEach(function(completion, i) {
    const scoreData = scores && scores[i] ? scores[i] : null;
    html += renderCompletionCard(completion, scoreData, i, n, dimNames);
  });

  if (scores && scores.length) {
    html += renderSummarySection(scores, dimNames);
    html += renderFlagsSection(scores);
  }

  if (result.session_id) {
    html += '<button class="download-btn" id="dl-btn">Download Markdown Report</button>';
  }

  container.innerHTML = html;

  const dlBtn = container.querySelector('#dl-btn');
  if (dlBtn) {
    dlBtn.addEventListener('click', function() {
      downloadReport(result.session_id, scenario.name);
    });
  }
}

function renderCompletionCard(completion, scoreData, index, n, dimNames) {
  const failed = scoreData && scoreData.flags && scoreData.flags.indexOf('scoring_failed') !== -1;
  const barsHtml = (scoreData && !failed)
    ? '<div class="score-bars">' + renderScoreBars(scoreData.scores, dimNames) + '</div>'
    : '';
  return '<div class="completion-card">' +
    '<div class="card-index">Sample ' + (index + 1) + ' of ' + n + '</div>' +
    '<pre class="card-text">' + escapeHtml(completion) + '</pre>' +
    barsHtml +
    '</div>';
}

function renderScoreBars(scores, dimNames) {
  return dimNames.map(function(dim) {
    const score = scores[dim];
    if (score === undefined || score < 0) {
      return '<div class="score-bar-row">' +
        '<span class="score-dim-label">' + escapeHtml(dim) + '</span>' +
        '<span class="score-failed-label">scoring failed</span></div>';
    }
    const pct = Math.round(score * 100);
    const color = scoreColor(score);
    return '<div class="score-bar-row">' +
      '<span class="score-dim-label">' + escapeHtml(dim) + '</span>' +
      '<div class="score-bar-track">' +
      '<div class="score-bar-fill" style="width:' + pct + '%;background-color:' + color + '"></div>' +
      '</div>' +
      '<span class="score-num">' + score.toFixed(2) + '</span>' +
      '</div>';
  }).join('');
}

function renderSummarySection(scores, dimNames) {
  const stats = computeStats(scores, dimNames);
  if (!Object.keys(stats).length) return '';

  const rows = Object.keys(stats).map(function(dim) {
    const s = stats[dim];
    const cls = s.mean > 0.6 ? ' class="cell-high"' : s.mean < 0.2 ? ' class="cell-low"' : '';
    return '<tr><td>' + escapeHtml(dim) + '</td>' +
      '<td' + cls + '>' + s.mean.toFixed(3) + '</td>' +
      '<td>' + s.min.toFixed(3) + '</td>' +
      '<td>' + s.max.toFixed(3) + '</td>' +
      '<td>' + s.std.toFixed(3) + '</td></tr>';
  }).join('');

  return '<div class="summary-section">' +
    '<div class="section-heading">Score Summary</div>' +
    '<table class="summary-table"><thead>' +
    '<tr><th>Dimension</th><th>Mean</th><th>Min</th><th>Max</th><th>Std</th></tr>' +
    '</thead><tbody>' + rows + '</tbody></table></div>';
}

function renderFlagsSection(scores) {
  const counts = {};
  scores.forEach(function(s) {
    (s.flags || []).forEach(function(f) {
      if (f !== 'scoring_failed') counts[f] = (counts[f] || 0) + 1;
    });
  });

  if (!Object.keys(counts).length) return '';

  const items = Object.keys(counts).map(function(f) {
    return '<span class="flag-item">' + escapeHtml(f) +
      '<span class="flag-count">' + counts[f] + 'x</span></span>';
  }).join('');

  return '<div class="flags-section">' +
    '<div class="section-heading">Flags</div>' +
    '<div class="flags-list">' + items + '</div></div>';
}

