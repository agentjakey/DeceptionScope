async function downloadReport(sessionId, scenarioName) {
  const text = await apiReport(sessionId);
  if (!text) {
    alert('No report found for this session. Reports are generated via the CLI report command.');
    return;
  }
  const blob = new Blob([text], { type: 'text/markdown' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = scenarioName + '_report.md';
  a.click();
  URL.revokeObjectURL(url);
}

