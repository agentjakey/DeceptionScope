function switchTab(tab) {
  state.activeTab = tab;

  document.querySelectorAll('.tab-btn').forEach(function(btn) {
    btn.classList.toggle('is-active', btn.dataset.tab === tab);
  });

  document.querySelectorAll('.tab-panel').forEach(function(panel) {
    panel.classList.remove('is-active');
  });

  const activePanel = document.getElementById('tab-' + tab);
  if (activePanel) activePanel.classList.add('is-active');

  // Clear selection when switching to multiplayer if current scenario is not multiplayer
  if (tab === 'multiplayer' && state.selectedScenario &&
      !state.selectedScenario.persuader_system_prompt) {
    state.selectedScenario = null;
  }

  renderSidebar(state.scenarios);
  updateButtonStates();
}

// ---------------------------------------------------------------------------
// Sidebar
// ---------------------------------------------------------------------------

function renderSidebar(scenarios) {
  const isMultiplayer = state.activeTab === 'multiplayer';
  const filtered = isMultiplayer
    ? scenarios.filter(function(s) { return !!s.persuader_system_prompt; })
    : scenarios;

  const html = filtered.map(function(s) {
    const threat = (s.threat_model || '').toLowerCase();
    const tooltip = THREAT_TOOLTIPS[threat] || '';
    const isSelected = state.selectedScenario && state.selectedScenario.name === s.name;
    return '<div class="scenario-item' + (isSelected ? ' is-selected' : '') +
      '" data-name="' + escapeHtml(s.name) + '">' +
      '<div class="scenario-item-title">' + escapeHtml(s.title || s.name) + '</div>' +
      '<div class="scenario-item-meta">' +
      '<span class="threat-tag" data-tooltip="' + escapeHtml(tooltip) + '">' +
      escapeHtml((s.threat_model || '').toUpperCase()) + '</span>' +
      '<span class="bon-label">N = ' + escapeHtml(String(s.bon_n || 1)) + '</span>' +
      '</div></div>';
  }).join('');

  setHTML('#scenario-list', html);

  document.querySelectorAll('.scenario-item').forEach(function(el) {
    el.addEventListener('click', function() {
      const s = scenarios.find(function(sc) { return sc.name === el.dataset.name; });
      if (s) selectScenario(s);
    });
  });
}

function selectScenario(scenario) {
  state.selectedScenario = scenario;
  renderSidebar(state.scenarios);

  if (state.activeTab === 'run') {
    renderScenarioDetail('#run-scenario-detail', scenario);
    const nInput = qs('#run-n');
    if (nInput) nInput.value = scenario.bon_n || 8;
  }

  updateButtonStates();
}

