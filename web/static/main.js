async function runMultiplayer() {
  const scenario = state.selectedScenario;
  const persuader = qs('#persuader-select').value;
  const skeptic = qs('#skeptic-select').value;
  if (!scenario) return;

  const resultsEl = qs('#multiplayer-results');
  resultsEl.innerHTML = RESEARCH_NOTE +
    '<div class="results-loading">Running multiplayer session -- this may take several minutes.</div>';

  const btn = qs('#multiplayer-btn');
  btn.disabled = true;
  btn.textContent = 'Running...';

  try {
    const session = await apiRunMultiplayer(scenario.name, persuader, skeptic);
    if (session.error) { showError(resultsEl, session); return; }
    renderMultiplayerResults(resultsEl, session);
  } catch (err) {
    showError(resultsEl, { error: err.message || 'Request failed' });
  } finally {
    btn.disabled = false;
    btn.textContent = 'Run Multiplayer Session';
    updateButtonStates();
  }
}

function renderMultiplayerResults(container, session) {
  let html = RESEARCH_NOTE + '<div class="multiplayer-transcript">';

  session.rounds.forEach(function(round) {
    const label = round.round_number === 0 ? 'Opening' : 'Round ' + round.round_number;
    html += '<div class="round-section">' +
      '<div class="round-label">' + escapeHtml(label) + '</div>' +
      '<div class="turn-block">' +
      '<div class="turn-role">Persuader [' + escapeHtml(session.persuader_model) + ']</div>' +
      '<div class="turn-text persuader">' + escapeHtml(round.persuader_output) + '</div>' +
      '</div>' +
      '<div class="turn-block">' +
      '<div class="turn-role">Skeptic [' + escapeHtml(session.skeptic_model) + ']</div>' +
      '<div class="turn-text skeptic">' + escapeHtml(round.skeptic_output) + '</div>' +
      '</div></div>';
  });

  html += '</div>';

  if (session.final_verdict) {
    // Escape first, then safely inject <strong> around the verdict line
    const escaped = escapeHtml(session.final_verdict);
    const bolded = escaped
      .replace(/Verdict: NO DECEPTION DETECTED/gi, '<strong>Verdict: NO DECEPTION DETECTED</strong>')
      .replace(/Verdict: DECEPTION DETECTED/gi, '<strong>Verdict: DECEPTION DETECTED</strong>');
    html += '<div class="verdict-box">' +
      '<div class="verdict-label">Final Verdict</div>' +
      '<div class="verdict-text">' + bolded + '</div>' +
      '</div>';
  }

  container.innerHTML = html;
}

// ---------------------------------------------------------------------------
// Init
// ---------------------------------------------------------------------------

async function init() {
  document.querySelectorAll('.tab-btn').forEach(function(btn) {
    btn.addEventListener('click', function() { switchTab(btn.dataset.tab); });
  });

  qs('#run-btn').addEventListener('click', runSession);
  qs('#compare-btn').addEventListener('click', runComparison);
  qs('#multiplayer-btn').addEventListener('click', runMultiplayer);

  try {
    const results = await Promise.all([
      fetch('/api/scenarios').then(function(r) { return r.json(); }),
      fetch('/api/models').then(function(r) { return r.json(); }),
    ]);
    state.scenarios = results[0];
    state.models = results[1];
  } catch (e) {
    setHTML('#scenario-list',
      '<div style="padding:20px;font-family:monospace;font-size:11px;color:#7A7570">' +
      'Could not reach the API. Is the server running?</div>');
    return;
  }

  renderSidebar(state.scenarios);
  renderRunModelGrid(state.models);

  // Auto-select haiku on page load if no model has been chosen yet.
  if (!state.runModel) {
    var defaultModel = state.models.find(function(m) {
      return m.alias === 'haiku' && m.key_configured;
    });
    if (!defaultModel) {
      defaultModel = state.models.find(function(m) { return m.key_configured; });
    }
    if (defaultModel) {
      state.runModel = defaultModel.alias;
      renderRunModelGrid(state.models);
    }
  }

  renderCompareModelGrid(state.models);
  renderMultiplayerSelects(state.models);
  updateButtonStates();
}

document.addEventListener('DOMContentLoaded', init);
