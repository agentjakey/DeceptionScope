function updateButtonStates() {
  const runBtn = qs('#run-btn');
  const compareBtn = qs('#compare-btn');
  const multiplayerBtn = qs('#multiplayer-btn');

  if (runBtn) {
    runBtn.disabled = !(state.selectedScenario && state.runModel);
  }
  if (compareBtn) {
    const n = state.compareModels.size;
    compareBtn.disabled = !(state.selectedScenario && n >= 2 && n <= 4);
  }
  if (multiplayerBtn) {
    multiplayerBtn.disabled = !(
      state.selectedScenario && state.selectedScenario.persuader_system_prompt
    );
  }
}

// ---------------------------------------------------------------------------
// Stats helpers
// ---------------------------------------------------------------------------

function computeStats(scores, dimNames) {
  const result = {};
  dimNames.forEach(function(dim) {
    const vals = scores
      .filter(function(s) {
        return s.flags.indexOf('scoring_failed') === -1 && s.scores[dim] >= 0;
      })
      .map(function(s) { return s.scores[dim]; });

    if (!vals.length) return;
    const mean = vals.reduce(function(a, b) { return a + b; }, 0) / vals.length;
    const min = Math.min.apply(null, vals);
    const max = Math.max.apply(null, vals);
    const std = vals.length > 1
      ? Math.sqrt(vals.reduce(function(a, v) { return a + Math.pow(v - mean, 2); }, 0) / vals.length)
      : 0;
    result[dim] = { mean: mean, min: min, max: max, std: std };
  });
  return result;
}

