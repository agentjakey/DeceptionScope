function groupByProvider(models) {
  const groups = {};
  models.forEach(function(m) {
    if (!groups[m.provider]) groups[m.provider] = [];
    groups[m.provider].push(m);
  });
  return groups;
}

function renderRunModelGrid(models) {
  const groups = groupByProvider(models);
  let html = '';

  Object.keys(groups).forEach(function(provider) {
    const mods = groups[provider];
    let btns = '';
    mods.forEach(function(m) {
      const selected = state.runModel === m.alias ? ' is-selected' : '';
      const disabled = m.key_configured ? '' : ' disabled';
      const keyNote = m.key_configured
        ? ''
        : ' <span class="key-not-set">(key not set)</span>';
      btns += '<button class="model-btn' + selected + '"' + disabled +
        ' data-alias="' + escapeHtml(m.alias) + '"' +
        ' title="' + escapeHtml(m.display_name) + ' - $' + m.cost_per_1k_tokens + '/1k">' +
        escapeHtml(m.alias) + keyNote + '</button>';
    });
    html += '<div class="provider-group">' +
      '<span class="provider-label">' + escapeHtml(provider) + '</span>' +
      '<div class="model-btn-row">' + btns + '</div>' +
      '</div>';
  });

  setHTML('#run-model-area', html);

  document.querySelectorAll('#run-model-area .model-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      state.runModel = btn.dataset.alias;
      renderRunModelGrid(state.models);
      updateButtonStates();
    });
  });
}

function renderCompareModelGrid(models) {
  const groups = groupByProvider(models);
  let html = '';

  Object.keys(groups).forEach(function(provider) {
    const mods = groups[provider];
    let rows = '';
    mods.forEach(function(m) {
      const checked = state.compareModels.has(m.alias) ? ' checked' : '';
      const disabled = m.key_configured ? '' : ' disabled';
      const labelClass = m.key_configured ? '' : ' is-disabled';
      const keyNote = m.key_configured ? '' : ' (key not set)';
      rows += '<div class="model-checkbox-row">' +
        '<input type="checkbox" class="model-checkbox"' + checked + disabled +
        ' id="cmp-' + escapeHtml(m.alias) + '"' +
        ' data-alias="' + escapeHtml(m.alias) + '">' +
        '<label class="model-checkbox-label' + labelClass + '" for="cmp-' + escapeHtml(m.alias) + '">' +
        escapeHtml(m.alias) + escapeHtml(keyNote) +
        '</label></div>';
    });
    html += '<div class="provider-group">' +
      '<span class="provider-label">' + escapeHtml(provider) + '</span>' +
      rows + '</div>';
  });

  setHTML('#compare-model-area', html);

  document.querySelectorAll('#compare-model-area .model-checkbox').forEach(function(cb) {
    cb.addEventListener('change', function() {
      if (cb.checked) {
        if (state.compareModels.size >= 4) { cb.checked = false; return; }
        state.compareModels.add(cb.dataset.alias);
      } else {
        state.compareModels.delete(cb.dataset.alias);
      }
      updateButtonStates();
    });
  });
}

function renderMultiplayerSelects(models) {
  const configured = models.filter(function(m) { return m.key_configured; });
  const options = configured.map(function(m) {
    return '<option value="' + escapeHtml(m.alias) + '">' + escapeHtml(m.alias) + '</option>';
  }).join('');

  const persuader = qs('#persuader-select');
  const skeptic = qs('#skeptic-select');
  if (persuader) persuader.innerHTML = options;
  if (skeptic) {
    skeptic.innerHTML = options;
    if (configured.length > 1) skeptic.value = configured[1].alias;
  }
}

